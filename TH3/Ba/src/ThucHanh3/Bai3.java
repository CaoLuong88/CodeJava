    private void btDongYSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDongYSuaActionPerformed
        // TODO add your handling code here:
        if (chisodong >= 0) {
            defaultTableModel.setValueAt(txtmaSVSua.getText(), chisodong, 1);
            defaultTableModel.setValueAt(txtHoTenSua.getText(), chisodong, 2);
            defaultTableModel.setValueAt(txtDienThoaiSua.getText(),chisodong, 3);
            defaultTableModel.setValueAt(txtEmailSua.getText(), chisodong, 4);
            tblDSSinhVien.setModel(defaultTableModel);
            tblDSSinhVien.clearSelection();
            resetControll();
            chisodong = -1;
            dialogSua.setVisible(false);
        }
    }//GEN-LAST:event_btDongYSuaActionPerformed

