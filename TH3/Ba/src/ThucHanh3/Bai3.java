    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        // TODO add your handling code here:
        String str = tblDSSinhVien.getModel().
                getValueAt(chisodong, 2).toString();
        int confirm = JOptionPane.showConfirmDialog(this,
                "Bạn chắc chắn muốn xóa bản ghi: \n" + str, "Confirm", JOptionPane.YES_NO_OPTION
        );
        if (confirm == JOptionPane.YES_OPTION) {
            defaultTableModel.removeRow(chisodong);
            tblDSSinhVien.setModel(defaultTableModel);
            chisodong = -1;
            tblDSSinhVien.clearSelection();
            resetControll();
        }
    }//GEN-LAST:event_btXoaActionPerformed

